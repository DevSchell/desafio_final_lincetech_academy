/// This Enum is about the possible experiences a user can tick while creating
/// an object "Trip"
enum EnumExperiencesList {
  /// This represents the experience of getting to know the place's culture
  culture,

  /// This represents the experiences of trying local food
  cuisine,

  ///This represents the experiences of hiking
  hiking,

  /// This represents the experiences of swimming in local places
  swimming,

  ///This represents the experiences of getting to know local wildlife
  wildlife,

  /// This represents the experiences fo visiting artistic hotspots
  art,

  /// This represents getting to know local ongoing festivals
  festivals,

  /// This represents the experience of going window shopping
  shopping,

  /// This represents the experience of having adventurous experiences
  adventure,
}
